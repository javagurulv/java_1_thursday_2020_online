package student_edgars_lukjanskis.lession_5;

class ForExample {

    public static void main(String[] args) {

        int i = 1; //for first part

        System.out.println(i);
        i++; // i = i + 1;

        if (i < 100) { //for second part
            System.out.println(i);
            i++; // for third part
        }

        if (i < 100) {
            System.out.println(i);
            i++;
        }

        if (i < 100) {
            System.out.println(i);
            i++;
        }

        //for ( ; ; ) {}


        for (int j = 1; j < 100; j++) {
            System.out.println(j);
        }






    }
}
