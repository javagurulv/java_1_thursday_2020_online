package student_vadims_vladisevs.lesson_15.level_2;

class Trader {

    String fullName;
    String city;

    public Trader(String name, String city){
        this.fullName = name;
        this.city = city;
    }

    public String getFullName() {
        return fullName;
    }

    public String getCity() {
        return city;
    }
}
