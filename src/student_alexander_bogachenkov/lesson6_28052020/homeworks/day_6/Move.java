package student_alexander_bogachenkov.lesson6_28052020.homeworks.day_6;

class Move {

    private int x;
    private int y;

    public Move(int xCoordinate, int yCoordinate) {
        this.x = xCoordinate;
        this.y = yCoordinate;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
