package student_deniss_gindins;

class MathOperationsDemo {
    public static void main(String[] args) {
        System.out.println("Let's repeat Math Again");

        int a = 26;
        int b = 13;
        int sum = a + b;
        int mult = a * b;
        int sub = a - b;
        int dev = a / b;

        System.out.println("Sum");
        System.out.print("a");
        System.out.print(" = ");
        System.out.print(a);
        System.out.print(", ");
        System.out.println("b = " + b);


        System.out.println("a + b = " + a + " + " + b + " = " + sum);
        System.out.println("a * b = " + a + " * " + b + " = " + mult);
        System.out.println("a - b = " + a + " - " + b + " = " + sub);
        System.out.println("a / b = " + a + " / " + b + " = " + dev);

    }
}